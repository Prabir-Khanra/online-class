#Logical Operator
# and
# or
# not

x = 45
y = 456

# ============================================================================================================
# and ============== returns True if both statements are true =========================================
#   True      False
# (x<y) and (x == y) # False
if (x == y) and (x < y) : #False
    print("AND")
else:
    print("I am in else part")

# ans: I am in else part






# =========================================================================================================
# or ============== Returns True if one of the statements is true =====================================
#   True      False
# (x<y) or (x == y) # True
if (x == y) or (x < y) : #True
    print("OR")
else:
    print("I am in else part")

# ans: OR

